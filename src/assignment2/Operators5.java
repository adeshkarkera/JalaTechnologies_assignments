//Programs on Logical AND,OR operator and Logical NOT
package assignment2;

public class Operators5 {

	public static void main(String[] args) {
		int a=10;  
		int b=5;  
		int c=20;  
		System.out.println(a<b&&a<c);//Logical AND
		System.out.println(a<b||a<c);//LogicalOR
		System.out.println(!(a<b&&a<c));//Logical NOT
	}

}
